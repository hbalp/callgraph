				      gfct_c2c <- Graph_func.G.add_edge_e gfct_c2c (Graph_func.G.E.create vcaller "external" vcallee);
				    )
				)
			      )
			    )
			  )
			)
			extcallers
		  )
		);
	      Some (fct_def, vcallee)
	    )
	)
    )

